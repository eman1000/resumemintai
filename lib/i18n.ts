export function tFactory(lang: LanguageCode) {
  const dict = { ...(BASE.en || {}), ...(BASE[lang] || {}) };
  return (key: string, fallback?: string) => dict[key] ?? fallback ?? key;
}

export function getMonths(lang: LanguageCode): string[] {
  const m = MONTHS[lang];
  if (!m || m.length === 0) return MONTHS.en;
  return m;
}

export const DEFAULT_SECTION_TITLES_KEYS = [
  "personalDetails","profile","employment","educations","skills","languages",
  "hobbies","qualities","courses","certificates","internships","sideActivities",
  "achievements","references","signature","footer",
] as const;

type CVSectionKey = typeof DEFAULT_SECTION_TITLES_KEYS[number];

export function defaultTitleKeyFor(sectionKey: CVSectionKey) {
  return `section.${sectionKey}`;
}

/**
 * Only change section titles that still match the previous language's default,
 * so we don't overwrite user-customized titles.
 */
export function localizeDocTitles<TDoc extends { sections: Array<{ key: CVSectionKey; title: string }> }>(
  doc: TDoc,
  lang: LanguageCode
): TDoc {
  const tNext = tFactory(lang);
  return {
    ...doc,
    sections: doc.sections.map((s) => ({
      ...s,
      title: tNext(defaultTitleKeyFor(s.key)),
    })),
  };
}

// Optional: skill level labels per language
export function getSkillLevels(lang: LanguageCode) {
  const t = tFactory(lang);
  return [
    t("level.beginner"),
    t("level.moderate"),
    t("level.good"),
    t("level.veryGood"),
    t("level.excellent"),
  ];
}
